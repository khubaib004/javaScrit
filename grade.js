let mark=parseInt(prompt("Enter the marks : "));

if (mark>=90)
    console.log("A+");
else if (mark>=80)
    console.log("A");
else if (mark>=70)
    console.log("A-");
else if (mark>=60)
    console.log("B");
else if (mark>=50)
    console.log("C");
else if (mark>=40)
    console.log("D");
else if (mark<=33)
    console.log("F");
